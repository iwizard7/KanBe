#!/bin/bash

# üéØ –û–Ω–ª–∞–π–Ω-—É—Å—Ç–∞–Ω–æ–≤—â–∏–∫ KanBe –¥–ª—è Raspberry Pi
# üì• –≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç —Å–∫–∞—á–∏–≤–∞–µ—Ç –∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –æ—Å–Ω–æ–≤–Ω–æ–π —Å–∫—Ä–∏–ø—Ç —É—Å—Ç–∞–Ω–æ–≤–∫–∏

# –¶–≤–µ—Ç–∞ –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞
print_step() {
    echo -e "${BLUE}üîÑ [–®–ê–ì] $1${NC}"
}

print_success() {
    echo -e "${GREEN}‚úÖ $1${NC}"
}

print_warning() {
    echo -e "${YELLOW}‚ö†Ô∏è  $1${NC}"
}

print_error() {
    echo -e "${RED}‚ùå $1${NC}"
}

print_info() {
    echo -e "${CYAN}‚ÑπÔ∏è  $1${NC}"
}

print_header() {
    echo -e "${PURPLE}üéØ === –û–Ω–ª–∞–π–Ω-—É—Å—Ç–∞–Ω–æ–≤—â–∏–∫ KanBe ===${NC}"
    echo ""
}

print_footer() {
    echo ""
    echo -e "${GREEN}üöÄ –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞! –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ KanBe!${NC}"
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –û–°
print_header

print_step "–ü—Ä–æ–≤–µ—Ä–∫–∞ –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º—ã..."
if [[ "$OSTYPE" != "linux-gnu"* ]]; then
    print_error "–≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –ø—Ä–µ–¥–Ω–∞–∑–Ω–∞—á–µ–Ω –¥–ª—è Linux (Debian –Ω–∞ Raspberry Pi)"
    echo "–¢–µ–∫—É—â–∞—è –û–°: $OSTYPE"
    exit 1
fi
print_success "–û–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–∞—è —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–æ–≤–µ—Ä–µ–Ω–∞"

# –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Ç–µ–∫—É—â–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
ORIGINAL_DIR="$(pwd)"
print_info "–ò—Å—Ö–æ–¥–Ω–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è: $ORIGINAL_DIR"

# –°–æ–∑–¥–∞–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –≤ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–º —Ö—Ä–∞–Ω–∏–ª–∏—â–µ
print_step "–°–æ–∑–¥–∞–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏..."
# –ò—Å–ø–æ–ª—å–∑—É–µ–º /var/tmp –≤–º–µ—Å—Ç–æ /tmp –¥–ª—è –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–≥–æ —Ö—Ä–∞–Ω–µ–Ω–∏—è
TEMP_DIR="/var/tmp/kanbe-install-$(date +%s)"
if ! mkdir -p "$TEMP_DIR"; then
    # –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–∞: –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –¥–æ–º–∞—à–Ω—é—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
    TEMP_DIR="$HOME/kanbe-install-$(date +%s)"
    if ! mkdir -p "$TEMP_DIR"; then
        print_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å –≤—Ä–µ–º–µ–Ω–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é"
        exit 1
    fi
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
if [ ! -d "$TEMP_DIR" ]; then
    print_error "–í—Ä–µ–º–µ–Ω–Ω–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ø–æ—Å–ª–µ —Å–æ–∑–¥–∞–Ω–∏—è"
    exit 1
fi

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –ø—Ä–∞–≤ –¥–æ—Å—Ç—É–ø–∞
chmod 755 "$TEMP_DIR"
print_success "–í—Ä–µ–º–µ–Ω–Ω–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è —Å–æ–∑–¥–∞–Ω–∞: $TEMP_DIR"

# –§—É–Ω–∫—Ü–∏—è –æ—á–∏—Å—Ç–∫–∏ –ø—Ä–∏ –≤—ã—Ö–æ–¥–µ
cleanup() {
    if [ -n "$TEMP_DIR" ] && [ -d "$TEMP_DIR" ]; then
        print_info "–û—á–∏—Å—Ç–∫–∞ –≤—Ä–µ–º–µ–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏..."
        rm -rf "$TEMP_DIR" 2>/dev/null || true
    fi
}
trap cleanup EXIT

# –ü–µ—Ä–µ—Ö–æ–¥ –≤ –≤—Ä–µ–º–µ–Ω–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
print_step "–ü–µ—Ä–µ—Ö–æ–¥ –≤ —Ä–∞–±–æ—á—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é..."
if ! cd "$TEMP_DIR"; then
    print_error "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–π—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é $TEMP_DIR"
    exit 1
fi
print_success "–†–∞–±–æ—á–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞"

# –ü—Ä–µ–¥–≤–∞—Ä–∏—Ç–µ–ª—å–Ω–∞—è –æ—á–∏—Å—Ç–∫–∞ –¥–ª—è –æ—Å–≤–æ–±–æ–∂–¥–µ–Ω–∏—è –º–µ—Å—Ç–∞
print_step "–ü–æ–¥–≥–æ—Ç–æ–≤–∫–∞ –º–µ—Å—Ç–∞ –¥–ª—è —Å–∫–∞—á–∏–≤–∞–Ω–∏—è..."
echo -e "${CYAN}üßπ –û—á–∏—Å—Ç–∫–∞ –∫—ç—à–µ–π –ø–µ—Ä–µ–¥ —Å–∫–∞—á–∏–≤–∞–Ω–∏–µ–º...${NC}"
sudo find /var/cache -type f -delete 2>/dev/null || true
sudo find /var/log -name '*.log' -type f -exec truncate -s 0 {} + 2>/dev/null || true

# –°–∫–∞—á–∏–≤–∞–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ —Å –ø–æ–≤—Ç–æ—Ä–Ω—ã–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏
print_step "–°–∫–∞—á–∏–≤–∞–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏..."
MAX_ATTEMPTS=3
for ((i=1; i<=MAX_ATTEMPTS; i++)); do
    echo -e "üì• –ü–æ–ø—ã—Ç–∫–∞ $i –∏–∑ $MAX_ATTEMPTS..."
    if curl -fsSL --connect-timeout 30 --max-time 300 -o "install.sh" "https://raw.githubusercontent.com/iwizard7/KanBe/main/install.sh"; then
        print_success "–°–∫—Ä–∏–ø—Ç —É—Å—Ç–∞–Ω–æ–≤–∫–∏ —É—Å–ø–µ—à–Ω–æ —Å–∫–∞—á–∞–Ω"
        break
    else
        print_warning "–û—à–∏–±–∫–∞ —Å–∫–∞—á–∏–≤–∞–Ω–∏—è –Ω–∞ –ø–æ–ø—ã—Ç–∫–µ $i"
        if [ $i -eq $MAX_ATTEMPTS ]; then
            print_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å —Å–∫—Ä–∏–ø—Ç –ø–æ—Å–ª–µ $MAX_ATTEMPTS –ø–æ–ø—ã—Ç–æ–∫"
            echo ""
            echo -e "${YELLOW}üîß –í–æ–∑–º–æ–∂–Ω—ã–µ —Ä–µ—à–µ–Ω–∏—è:${NC}"
            echo "1. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç—É"
            echo "2. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –ª–æ–∫–∞–ª—å–Ω—É—é –∫–æ–ø–∏—é"
            echo "3. –°–∫–∞—á–∞–π—Ç–µ —Å–∫—Ä–∏–ø—Ç –≤—Ä—É—á–Ω—É—é: curl -o install.sh https://raw.githubusercontent.com/iwizard7/KanBe/main/install.sh"
            exit 1
        fi
        echo "‚è≥ –ü–æ–≤—Ç–æ—Ä–Ω–∞—è –ø–æ–ø—ã—Ç–∫–∞ —á–µ—Ä–µ–∑ 2 —Å–µ–∫—É–Ω–¥—ã..."
        sleep 2
    fi
done

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è —Ñ–∞–π–ª–∞
if [ ! -f "install.sh" ]; then
    print_error "–§–∞–π–ª install.sh –Ω–µ –Ω–∞–π–¥–µ–Ω –ø–æ—Å–ª–µ —Å–∫–∞—á–∏–≤–∞–Ω–∏—è"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–∞–∑–º–µ—Ä–∞ —Ñ–∞–π–ª–∞
if [ ! -s "install.sh" ]; then
    print_error "–°–∫–∞—á–∞–Ω–Ω—ã–π —Ñ–∞–π–ª install.sh –ø—É—Å—Ç–æ–π"
    exit 1
fi

print_success "–§–∞–π–ª —Å–∫—Ä–∏–ø—Ç–∞ –ø—Ä–æ–≤–µ—Ä–µ–Ω"

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∞–≤ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ
print_step "–ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø—Ä–∞–≤ –¥–æ—Å—Ç—É–ø–∞..."
chmod +x install.sh

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è
if [ ! -x "install.sh" ]; then
    print_error "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –ø—Ä–∞–≤–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –Ω–∞ install.sh"
    exit 1
fi
print_success "–ü—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞ –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã"

# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞
check_disk_space() {
    local min_space_mb=500  # –ú–∏–Ω–∏–º–∞–ª—å–Ω–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ–µ –º–µ—Å—Ç–æ –≤ –ú–ë

    echo -e "${CYAN}üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞ –Ω–∞ –¥–∏—Å–∫–µ...${NC}"

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–µ—Å—Ç–æ –≤ –∫–æ—Ä–Ω–µ–≤–æ–º —Ä–∞–∑–¥–µ–ª–µ (–ø–æ—Å—Ç–æ—è–Ω–Ω–æ–µ —Ö—Ä–∞–Ω–∏–ª–∏—â–µ)
    local root_space=$(df / | awk 'NR==2 {print int($4/1024)}')
    local root_device=$(df / | awk 'NR==2 {print $1}')
    echo "–û—Å–Ω–æ–≤–Ω–æ–µ —Ö—Ä–∞–Ω–∏–ª–∏—â–µ ($root_device): ${root_space}MB —Å–≤–æ–±–æ–¥–Ω–æ"

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–µ—Å—Ç–æ –≤ /var/tmp (–µ—Å–ª–∏ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç)
    if [ -d "/var/tmp" ]; then
        local var_space=$(df /var/tmp | awk 'NR==2 {print int($4/1024)}')
        echo "–•—Ä–∞–Ω–∏–ª–∏—â–µ /var/tmp: ${var_space}MB —Å–≤–æ–±–æ–¥–Ω–æ"
    fi

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–µ—Å—Ç–æ –≤ –¥–æ–º–∞—à–Ω–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
    local home_space=$(df "$HOME" | awk 'NR==2 {print int($4/1024)}')
    echo "–î–æ–º–∞—à–Ω—è—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è: ${home_space}MB —Å–≤–æ–±–æ–¥–Ω–æ"

    # –ò—Å–ø–æ–ª—å–∑—É–µ–º –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Å—Ä–µ–¥–∏ –ø–æ—Å—Ç–æ—è–Ω–Ω—ã—Ö —Ö—Ä–∞–Ω–∏–ª–∏—â
    local available_space=$root_space
    if [ -d "/var/tmp" ] && [ "$var_space" -gt "$available_space" ]; then
        available_space=$var_space
    fi
    if [ "$home_space" -gt "$available_space" ]; then
        available_space=$home_space
    fi

    echo "–ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: ${available_space}MB"

    if [ "$available_space" -lt "$min_space_mb" ]; then
        echo -e "${YELLOW}‚ö†Ô∏è  –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –º–µ—Å—Ç–∞ –≤ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–º —Ö—Ä–∞–Ω–∏–ª–∏—â–µ (${available_space}MB < ${min_space_mb}MB)${NC}"

        # –ü–æ–ø—ã—Ç–∫–∞ –æ—á–∏—Å—Ç–∫–∏ –≤—Ä–µ–º–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤ –≤ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–º —Ö—Ä–∞–Ω–∏–ª–∏—â–µ
        echo -e "${CYAN}üßπ –ü–æ–ø—ã—Ç–∫–∞ –æ—á–∏—Å—Ç–∫–∏ –≤—Ä–µ–º–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤...${NC}"

        # –û—á–∏—â–∞–µ–º /var/tmp –µ—Å–ª–∏ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
        if [ -d "/var/tmp" ]; then
            sudo find /var/tmp -type f -mtime +1 -delete 2>/dev/null || true
            sudo find /var/tmp -type d -mtime +1 -exec rm -rf {} + 2>/dev/null || true
        fi

        # –û—á–∏—â–∞–µ–º –∫—ç—à –≤ –¥–æ–º–∞—à–Ω–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
        if [ -d "$HOME/.npm" ]; then
            npm cache clean --force 2>/dev/null || true
        fi

        # –û—á–∏—â–∞–µ–º —Å–∏—Å—Ç–µ–º–Ω—ã–µ –∫—ç—à–∏
        sudo apt-get clean 2>/dev/null || true
        sudo apt-get autoclean 2>/dev/null || true

        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏
        root_space=$(df / | awk 'NR==2 {print int($4/1024)}')
        available_space=$root_space

        if [ -d "/var/tmp" ]; then
            var_space=$(df /var/tmp | awk 'NR==2 {print int($4/1024)}')
            if [ "$var_space" -gt "$available_space" ]; then
                available_space=$var_space
            fi
        fi

        echo "–°–≤–æ–±–æ–¥–Ω–æ–µ –º–µ—Å—Ç–æ –ø–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏: ${available_space}MB"

        if [ "$available_space" -lt "$min_space_mb" ]; then
            echo -e "${RED}‚ùå –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏ –Ω–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –º–µ—Å—Ç–∞ –≤ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–º —Ö—Ä–∞–Ω–∏–ª–∏—â–µ${NC}"
            echo ""
            echo -e "${YELLOW}üîß –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –ø–æ –æ—Å–≤–æ–±–æ–∂–¥–µ–Ω–∏—é –º–µ—Å—Ç–∞:${NC}"
            echo ""
            echo "1. –û—á–∏—Å—Ç–∏—Ç–µ –∫—ç—à –ø–∞–∫–µ—Ç–Ω–æ–≥–æ –º–µ–Ω–µ–¥–∂–µ—Ä–∞:"
            echo "   sudo apt-get clean && sudo apt-get autoclean"
            echo "   sudo apt-get autoremove"
            echo ""
            echo "2. –û—á–∏—Å—Ç–∏—Ç–µ npm –∫—ç—à:"
            echo "   npm cache clean --force"
            echo ""
            echo "3. –£–¥–∞–ª–∏—Ç–µ –Ω–µ–Ω—É–∂–Ω—ã–µ —Ñ–∞–π–ª—ã –∏–∑ /var/tmp:"
            echo "   sudo find /var/tmp -type f -mtime +1 -delete"
            echo ""
            echo "4. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –¥–∏—Å–∫–∞:"
            echo "   df -h"
            echo "   du -sh /var/* /home/* 2>/dev/null | sort -hr | head -10"
            echo ""
            echo "5. –£–≤–µ–ª–∏—á—å—Ç–µ —Ä–∞–∑–º–µ—Ä —Ä–∞–∑–¥–µ–ª–∞ (–µ—Å–ª–∏ –≤–æ–∑–º–æ–∂–Ω–æ):"
            echo "   sudo raspi-config (Advanced Options -> Expand Filesystem)"
            echo ""
            echo -e "${YELLOW}–¢—Ä–µ–±—É–µ—Ç—Å—è –º–∏–Ω–∏–º—É–º ${min_space_mb}MB —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞ –≤ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–º —Ö—Ä–∞–Ω–∏–ª–∏—â–µ${NC}"
            exit 1
        fi
    fi

    print_success "–î–∏—Å–∫–æ–≤–æ–µ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ –ø—Ä–æ–≤–µ—Ä–µ–Ω–æ (${available_space}MB –¥–æ—Å—Ç—É–ø–Ω–æ)"
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∏—Å–∫–æ–≤–æ–≥–æ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–∞ –ø–µ—Ä–µ–¥ —É—Å—Ç–∞–Ω–æ–≤–∫–æ–π
check_disk_space

# –§–∏–Ω–∞–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –ø–µ—Ä–µ–¥ –∑–∞–ø—É—Å–∫–æ–º
echo ""
echo -e "${CYAN}üìã –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –∑–∞–ø—É—Å–∫–µ:${NC}"
echo "   –¢–µ–∫—É—â–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è: $(pwd)"
echo "   –§–∞–π–ª —Å–∫—Ä–∏–ø—Ç–∞: $(ls -la install.sh)"
